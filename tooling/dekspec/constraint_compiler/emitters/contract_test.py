"""contract_test emitter — IC IR -> pytest module string.

Generates a test module per IC. All auto-generated tests are SKIPPED by
default with rich docstrings carrying the contract claim verbatim. The
engineer's job is to replace the `pytest.skip(...)` body with a real
assertion (or to dispute the claim via /write-ic --review).

Tests reference per-operation pytest fixtures the consumer must provide
in a conftest.py (e.g., `evaluate_impl` for an `evaluate` operation).
This keeps the emitter free of consumer-specific import paths.

Public API: emit(ir) -> str, suggested_filename(ir) -> str

ROADMAP — current state is intentional, not a missing feature
============================================================
Every assertion body is `pytest.skip("CONTRACT_STUB: ...")`. That is a
deliberate v0.x design choice and was flagged as such in the 2026-05-11
system audit (divergence D-39): the emitter is structurally a stub
generator today, not yet an executable contract-enforcement generator.

Planned upgrade path (no committed timeline):

  Phase 1 (today): skip-stubs with rich docstrings carrying the contract
    claim verbatim. The engineer authors real assertions in a sibling
    module that imports the fixtures.

  Phase 2 (TBD): emit type-narrowed assertion templates for the subset
    of operation shapes where the IC is fully self-describing (return
    type + invariants + error semantics all schema-typed). Skip-stubs
    remain for operations the IC describes only in prose.

  Phase 3 (TBD, possibly never): full executable contracts — runtime
    fixtures (HTTP / process / message bus) emitted from the IC's
    party kind. This crosses the consumer-isolation boundary and may
    instead become the DekFactory executor's responsibility.

Until Phase 2 lands, do not interpret a green contract-test suite as
evidence that the implementation conforms to the IC — every assertion
is `pytest.skip` and every test passes vacuously. The emitter is for
*scaffolding*; real enforcement lives in the hand-written sibling.
"""

from __future__ import annotations

import re
from typing import Any

from .. import IR_SCHEMA_VERSION, PARSER_VERSION

EMITTER_VERSION = "0.1.0"
_SLUG_NONWORD = re.compile(r"[^a-z0-9]+")


def emit(ir: dict[str, Any]) -> str:
    """Emit a pytest module string for the given IC IR.

    Output is a self-contained Python source string; the caller writes it
    to disk under whatever path the CLI / persistence layer chooses.
    """
    parts: list[str] = []
    parts.append(_module_docstring(ir))
    parts.append("")
    parts.append("from __future__ import annotations")
    parts.append("")
    parts.append("import pytest")
    parts.append("")

    operations = ir.get("interface_definition", {}).get("operations", [])
    if not operations:
        parts.append(_no_operations_block(ir))
    else:
        for op in operations:
            parts.append(_operation_section(op))

    parts.append(_error_semantics_section(ir, operations))
    parts.append(_consistency_section(ir, operations))
    parts.append(_does_not_hold_block(ir))
    parts.append(_parse_warnings_block(ir))

    return "\n".join(p for p in parts if p is not None) + "\n"


def suggested_filename(ir: dict[str, Any]) -> str:
    """Return the conventional filename for this IC's contract test module."""
    ic_id = ir["id"].lower().replace("-", "_")
    name_slug = _slug(ir.get("name", ""))
    return f"test_contract_{ic_id}_{name_slug}.py"


